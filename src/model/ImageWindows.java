package model;

public class ImageWindows {
	private String nomImageW, typeImageW;

	public ImageWindows(String nomImageW, String typeImageW) {
		super();
		this.nomImageW = nomImageW;
		this.typeImageW = typeImageW;
	}

	public String getNomImageW() {
		return nomImageW;
	}

	public void setNomImageW(String nomImageW) {
		this.nomImageW = nomImageW;
	}

	public String getTypeImageW() {
		return typeImageW;
	}

	public void setTypeImageW(String typeImageW) {
		this.typeImageW = typeImageW;
	}

}
